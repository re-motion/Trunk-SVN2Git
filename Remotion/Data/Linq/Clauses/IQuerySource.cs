using System;

namespace Remotion.Data.Linq.Clauses
{
  /// <summary>
  /// Represents a clause or result operator that generates items which are streamed to the following clauses or operators.
  /// </summary>
  public interface IQuerySource
  {
    /// <summary>
    /// Gets or sets a name describing the items generated by this <see cref="IQuerySource"/>.
    /// </summary>
    string ItemName { get; }

    /// <summary>
    /// Gets or sets the type of the items generated by this <see cref="IQuerySource"/>.
    /// </summary>
    Type ItemType { get; }
  }
}